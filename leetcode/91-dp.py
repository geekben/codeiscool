class Solution(object):
    def numDecodings(self, s):
        """
        :type s: str
        :rtype: int
        """
        l = len(s)
        f = [0 for _ in xrange(l+1)]
        f[0] = 1
        if s[0] > '0' and s[0] <= '9':
            f[1] = 1

        for i,c in enumerate(s[1:]):
            if c > '0' and c <= '9':
                f[i+2] = f[i+1]
            if s[i] == '1' and c >= '0' and c <= '9':
                f[i+2] += f[i]
            if s[i] == '2' and c >= '0' and c <= '6':
                f[i+2] += f[i]
        return f[l]
